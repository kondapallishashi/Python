#Demonstrates the string data types in python
#Strings can be created using single quotes(''), double quotes ("")and three quotes (''' or """)

str = "Welcome to Python programming"

#substrings can be formed by the [] operator or [:] operator
print(str)
print("To print the first, third and tenth character in the string use str[0],str[2],str[9]", str[0], str[2], str[9])
print("to print the sentence from 9th character till the end use str[9:]", str[9:])
print("To print the sentence from 5th character till 15th character use str[4:14]", str[4:14])
print("To repeat the same sentence twice we can use str*2 ", str*2)
