#Demonstrates the usage of variables in python.
#variables are assigned with assigment (=) operator where the left operand is the variable and the right operand is the value.

#assigning an integer value
length = 5
#Assigning a floating point number
breadth = 10.0
#Assigning a string 
shape = "rectangle"

#printing the values
print(length)
