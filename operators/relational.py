#Demonstrates the usage of relational or comparision operators in python.
x = 10
y = 5

# == is equal to provides a logical result of True or False based on whether operands are equal or not
print("The result of ", x, " == ", y, " is: ", x == y)
print("The result of ", x, " != ", y, " is: ", x != y)
print("The result of ", x, " > ", y, " is: ", x > y)
print("The result of ", x, " >= ", y, " is: ", x >= y)
print("The result of ", x, " < ", y, " is: ", x < y)
print("The result of ", x, " <= ", y, " is: ", x <= y)
