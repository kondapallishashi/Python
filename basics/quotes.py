#This program demonstrates the support of quotes in python program
#Python supports single (''), double ("") and triple quotes (''' or """) as long as the same type of quote is used to start and end the string.

print('This sentence is enclosed in single quotes')
print("This sentence is enclosed in double quotes")
print('''This sentence is a multi-line
sentence that uses three single quotes''')

print("""This sentence is also another multi-line sentence
that uses three double quotes 
to start and end the string""")

print("double quotes are used to enclose the string as ' single quotes' are interspersed in the statement")
